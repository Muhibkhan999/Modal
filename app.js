  var modalParentElm = document.getElementsByClassName('modal-parent')[0];
  var modalElm = document.getElementsByClassName('modal')[0];
  var mainElm = document.getElementsByClassName('main')[0];
  
  // Open the modal
  function openModal() {
    modalParentElm.style.display = 'flex';
    setTimeout(() => modalParentElm.style.opacity = '1', 10);  // Adding slight delay for fade-in effect
  }
  
  // Close the modal
  function closeModal() {
    modalParentElm.style.opacity = '0';  // Fade-out effect
    setTimeout(() => modalParentElm.style.display = 'none', 300); // Hide modal after fade-out
    // Clear the modal body for the next click
    var modalBdy = document.getElementsByClassName('modal-body')[0];
    if (modalBdy) {
      modalElm.removeChild(modalBdy);
    }
  }
  
  // Show group details in modal
  function clickedGroup(gName) {
    openModal();
  
    // Find the selected group
    var thisData = data[0].groups.find(group => group.name === gName);
  
    // Create modal body and add it to modal
    var modalBdy = document.createElement('div');
    modalBdy.setAttribute('class', 'modal-body');
    modalElm.appendChild(modalBdy);
  
 
    var tbl = document.createElement('table');
    tbl.innerHTML = `
      <tr>
        <th>Team</th>
        <th>Code</th>
        <th>Played</th>
        <th>Won</th>
        <th>Drawn</th>
        <th>Lost</th>
        <th>Goals For</th>
        <th>Goals Against</th>
        <th>Points</th>
      </tr>
    `;
    modalBdy.appendChild(tbl);
  
    // Add each team's details in the group to the table
    thisData.standings.forEach(standing => {
      var tr = document.createElement('tr');
      tr.innerHTML = `
        <td>${standing.team.name}</td>
        <td>${standing.team.code}</td>
        <td>${standing.played}</td>
        <td>${standing.won}</td>
        <td>${standing.drawn}</td>
        <td>${standing.lost}</td>
        <td>${standing.goals_for}</td>
        <td>${standing.goals_against}</td>
        <td>${standing.pts}</td>
      `;
      tbl.appendChild(tr);
    });
  }
  
  // Generate the group buttons dynamically
  function createGroupButtons() {
    var groupContainer = document.createElement('div');
    groupContainer.classList.add('group-container');
  
    data[0].groups.forEach(group => {
      var groupDiv = document.createElement('div');
      groupDiv.classList.add('groupDiv');
      groupDiv.textContent = group.name;
      groupDiv.addEventListener('click', () => clickedGroup(group.name));
      groupContainer.appendChild(groupDiv);
    });
  
    // Append the group buttons to the main element
    mainElm.appendChild(groupContainer);
  }
  
  // Initialize group buttons
  createGroupButtons();
  